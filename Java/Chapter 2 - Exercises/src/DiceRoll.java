
public class DiceRoll {
	public static void main(String[] args) { // takes in a String array named args
		// void: doesn't return anything
		// static: called without an object, method of the class, not of an
		//		   instance of the object (have no declarer, just not listed as static)
		
		int dice1;
		int dice2;
		dice1 = (int)(Math.random()*6) + 1; // Math.random returns float between 0-1,
		// multiply by 6 to get between 0-5 and then add 1 (type-cast has highest precedence)
		dice2 = (int)(Math.random()*6) + 1;
		System.out.println("The first die comes up " + dice1);
		System.out.println("The second die comes up " + dice2);
		System.out.println("Your total roll is " + (dice1 + dice2));
		
	}

}
